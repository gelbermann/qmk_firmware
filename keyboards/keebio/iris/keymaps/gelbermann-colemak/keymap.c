bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case COLEMAK_DHM:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_COLEMAK_DHM);
      }
      return false;
      break;
    case HEBREW:    // TODO re-do this case?
      if (record->event.pressed) {
        set_single_persistent_default_layer(_HEBREW);
      }
      return false;
      break;
    case HEBREW:    // TODO re-do this case?
      if (record->event.pressed) {
        set_single_persistent_default_layer(_HEBREW);
      }
      return false;
      break;
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _NAV);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _NAV);
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _NAV);
      } else {
        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _NAV);
      }
      return false;
      break;
    case NAV:
      if (record->event.pressed) {
        layer_on(_NAV);
      } else {
        layer_off(_NAV);
      }
      return false;
      break;
  }
  return true;
}

bool encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) {
        if (clockwise) {
            tap_code(KC_VOLU);
        } else {
            tap_code(KC_VOLD);
        }
    }
    else if (index == 1) {
        if (clockwise) {
            tap_code(KC_PGDN);
        } else {
            tap_code(KC_PGUP);
        }
    }
    return true;
}
